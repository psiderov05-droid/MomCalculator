  combined_array = combining_arrays(array_for_first_letter, array_for_letter_in_name_1, array_for_letter_in_name_2, array_for_letter_in_name_3, array_for_last_letter);
  
  
  word = filtiering_combined_array(combined_array, times_to_be_found);

